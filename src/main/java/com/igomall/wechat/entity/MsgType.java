package com.igomall.wechat.entity;

/**
 * 消息类型
 */
public enum MsgType {

    /**
     * 文本消息
     */
    text,


    /**
     * 图片消息
     */
    image,


    /**
     * 语音消息
     */
    voice,


    /**
     * 视频消息
     */
    video,


    /**
     * 小视频消息
     */
    shortvideo,


    /**
     * 地理位置消息
     */
    location,


    /**
     * 链接消息
     */
    link,


    /**
     * 事件
     */
    event,


    /**
     * 回复音乐消息
     */
    music,


    /**
     * 回复图文消息
     */
    news,
}
